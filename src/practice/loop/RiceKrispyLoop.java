package practice.loop;

public class RiceKrispyLoop 
{
	public static void main(String[] args) 
	{
		for(  int k = 0; k < 4; k++)
		{
			System.out.println("Snap");
			for( int f = 0; f < 3; f++)
			{
				System.out.println("Crackle");
				for( int r = 0; r < 2; r++)
				{
					System.out.println("Pop");
				}
			}
		}
		

	}

}
